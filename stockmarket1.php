<font size= "3">The stock exchange is an organized and regulated financial market where securities like bonds, notes and shares are bought and sold at prices governed by the forces of demand and supply.
Stock exchanges serve as primary markets where the corporations, governments and other incorporated bodies can raise capital by channeling savings of the investors into productive ventures.
Trades in the older exchanges used to conduct on the floor (called the ‘trading floor') of the exchange itself. On modern exchanges, trades are conducted over telephone or online. Due to the ease of trading,
there is an increase of people involved in the purchase and selling of the shares.
The stock exchange consists of a lot of data like the registered company’s data, trader’s data, broker’s details, share prices and much more. Hence, it is necessary to record and maintain the data that is generated every day.
